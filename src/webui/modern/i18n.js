export function t(lang, key, vars = {}) {
  const l = lang === "zh" ? "zh" : "en";
  const primary = STRINGS[l] || STRINGS.en;
  let out = primary[key] ?? STRINGS.en[key] ?? String(key || "");
  for (const [k, v] of Object.entries(vars || {})) {
    out = out.replaceAll(`{${k}}`, String(v ?? ""));
  }
  return out;
}
